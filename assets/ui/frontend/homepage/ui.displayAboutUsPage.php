<h1>Frequently Asked Questions</h1><br />

<h3>Question 1:</h3>
<p class="justify">‘My daughter spilt air freshener liquid on the arm of my leather sofa and the colour has come out; what happens now?’</p>

<h3>Answer:</h3>
<p class="justify">To put the colour back into the arm of your leather sofa the affected area would need to be degreased then re-coloured and re-lacquered.</p>

<h3>Question 2:</h3>
<p class="justify">‘When I sit down the seats tilt to one side – what can be done?’</p>

<h3>Answer:</h3>
<p class="justify">The interior has flattened; your sofa is probably used in one area more than the other hence the tilting.  There are two options here.  The first is to re-boost the original seating with more foam/fibre filling or we can replace with new interiors.</p>

<h3>Question 3:</h3>
<p class="justify">‘The arms on my recliner chair are loose and catching on the seat, what should I do?’</p>

<h3>Answer:</h3>
<p class="justify">Most recliner sofas have ‘bolt on’ arms which can work loose.  These can be stripped down, re-tightened and re-strengthened to make good again.</p>

